#include "libft.h"

int	ft_atoi(char *str)
{
	int me;
	int sign;
	sign = 1;

	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		me = me * 10 + (*str++ - '0');
	return (me * sign);
}

